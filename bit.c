#include <stdio.h>

int main() 
{
    int a = 5;  
    int b = 3;   

    // Bitwise AND
    printf("a & b  = %d\n", a & b);

    // Bitwise OR
    printf("a | b  = %d\n", a | b);

    // Bitwise XOR
    printf("a ^ b  = %d\n", a ^ b);

    // Bitwise NOT
    printf("~a     = %d\n", ~a);
    printf("~b     = %d\n", ~b);
}